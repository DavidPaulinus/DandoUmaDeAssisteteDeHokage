
public class Ninja {
	private String nome;
	private String aldeia;
	
	
	public Ninja(String nome, String aldeia) {
		super();
		this.nome = nome;
		this.aldeia = aldeia;
	}
	
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	
	public String getAldeia() {
		return aldeia;
	}
	public void setAldeia(String aldeia) {
		this.aldeia = aldeia;
	}
	
	
}
